export function add(a, b) {
  return a + b;
}

export const mul = (a, b) => {
  return a * b;
};

export function div(a, b) {
  return a / b;
}

export function mod(a, b) {
  return a % b;
}

function subtract(a, b) {
  return a - b;
}

export default subtract;

